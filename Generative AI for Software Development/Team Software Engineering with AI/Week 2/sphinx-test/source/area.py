def calculate_area(radius):
    """
        Calculate the area of a circle given its radius.

        :param radius: The radius of the circle.
        :type radius: float

        :return: The area of the circle.
        :rtype: float

        :Example:

        >>> calculate_area(5)
        78.53975
    """
    pi = 3.14159
    return pi * radius * radius
